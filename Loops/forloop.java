package ControlStructure.Loops;
import java.util.Scanner;
public class forloop {
    public static void main(String[] args) {
        int i,n,b;
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter number");
        n=sc.nextInt();
        for(i=1;i<=0;i++){
            b=n*1;
            System.out.println(b);
        }
    }
}
